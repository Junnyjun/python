class  calculator:
    def __init__(self):
        self.data = 0

    def add(self, add_number):
        self.data += add_number

    def minus(self, add_number):
        self.data -= add_number

    def multi(self, add_number):
        self.data *= add_number

    def div(self, add_number):
        self.data /= add_number